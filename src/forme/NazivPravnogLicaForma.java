    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
if(jTextField1.getText().isEmpty()){
    JOptionPane.showMessageDialog(rootPane, "Niste uneli naziv");
    return;
}
String naziv=jTextField1.getText();
PravnoLice pl=Kontroler.getInstanca().nadjiPravnoLice(naziv);
if(pl==null){
  JOptionPane.showMessageDialog(rootPane, "Ne postoji to pravno lice u bazi! Prvo ga kreirajte! ");
    DodajPravnoLiceForm dpl=new DodajPravnoLiceForm();
    dpl.setPreIzmene(pl);
    dpl.setTrazenNaziv(true);
    dpl.setNaziv(naziv);
    dpl.setIzmena(false);     
       dpl.srediFormu(null,false);
           this.setVisible(false);

    dpl.setVisible(true); 
}else
{
     
    DodajZahtevForm dz=new DodajZahtevForm();
    this.setVisible(false);
    dz.setPravnoLice(pl);
           dz.srediFormu(null, true);
    dz.setVisible(true);
}

    }//GEN-LAST:event_jButton1ActionPerformed

