    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int red=tabelaPravnaLica.getSelectedRow();
        if(red!=-1){
      
            ModelTabelePravnaLica mtpl=(ModelTabelePravnaLica) tabelaPravnaLica.getModel();
           PravnoLice pl= mtpl.vratiPravnoLice(red);
            
    boolean uspesno=Kontroler.getInstanca().obrisiPravnoLive(pl);
if(uspesno){
                mtpl.obrisi(red);
        JOptionPane.showMessageDialog(this, "Uspesno obrisano!");
        return;
        

}else {
        JOptionPane.showMessageDialog(this, "Nije uspesno obrisano!");
return;
}
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Niste izabrali pravno lice!");
            return;
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        int red=tabelaPravnaLica.getSelectedRow();
        if(red!=-1){
            DodajPravnoLiceForm dpl=new DodajPravnoLiceForm();
            ModelTabelePravnaLica mtpl=(ModelTabelePravnaLica) tabelaPravnaLica.getModel();
            PravnoLice pl=mtpl.vratiPravnoLice(red);
            dpl.setIzmena(false);
            dpl.srediFormu(pl,true);
            dpl.setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Niste izabrali pravno lice!");
            return;
        }
    }//GEN-LAST:event_btnPrikaziActionPerformed

