    private void srediTabelu() {
ArrayList<PravnoLice> lista=new ArrayList<>();
if(jComboBox1Vrsta.getSelectedIndex()!=-1){
if(jComboBox1Vrsta.getSelectedIndex()==0){
lista=Kontroler.getInstanca().vratiMalePrihode();
}
if(jComboBox1Vrsta.getSelectedIndex()==1){
lista=Kontroler.getInstanca().vratiSrednjePrihode();
}
if(jComboBox1Vrsta.getSelectedIndex()==2){
lista=Kontroler.getInstanca().vratiVelikePrihode();
}

}else {
lista=Kontroler.getInstanca().vratiMalePrihode();

}
if(lista!=null){

ModelTabelePravnaLica mtpl=new ModelTabelePravnaLica(lista);
tabelaPravnaLica.setModel(mtpl);
}else{
ModelTabelePravnaLica mtpl=new ModelTabelePravnaLica(new ArrayList<PravnoLice>());
tabelaPravnaLica.setModel(mtpl);
}
    }

    private void pripremaZaSort() {
TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(tabelaPravnaLica.getModel());
        tabelaPravnaLica.setRowSorter(rowSorter);

        txtFilter.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(javax.swing.event.DocumentEvent e) {
                String filter = txtFilter.getText();

                if (filter.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + filter));
                }
            }

            @Override
            public void removeUpdate(javax.swing.event.DocumentEvent e) {
                String text = txtFilter.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(javax.swing.event.DocumentEvent e) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }
